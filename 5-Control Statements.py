# Control Statements

pet = "Bird"

if pet == "Cat":
    print("I have a cat.It's name is Kitty.")
elif pet == "Dog":
    print("I have a dog.It's name is Tommy.")
elif pet == "Parrot":
    print("I have a parrot.It's name is Pully.")
elif pet == "Rabbit":
    print("I have a rabbit.It's name is Bobbie")
else:
    print("I have a pet")